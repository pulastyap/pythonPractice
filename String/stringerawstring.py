
newline = r"Hello\nWorld"
tab = r"Hello\tWorld"
backslash = r"Hello\\World"
single_quote = r"Hello\'World"
double_quote = r"Hello\"World"

path = r"C:\Users\Name"

print(newline)              # Output: Hello\nWorld
print(tab)                  # Output: Hello\tWorld
print(backslash)            # Output: Hello\\World
print(single_quote)         # Output: Hello\'World
print(double_quote)         # Output: Hello\"World
print(path)                 # Output: C:\Users\Name

