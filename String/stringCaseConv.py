first_name = "Pulastya"
last_name = "pandey"
full_name = "pulastya pandey"

print(first_name.lower())               # Output: pulastya
print(first_name.upper())               # Output: PULASTYA
print(last_name.capitalize())           # Output: Pandey
print(full_name.title())                # Output: Pulastya Pandey
print(first_name.swapcase())            # Output: pULASTYA


