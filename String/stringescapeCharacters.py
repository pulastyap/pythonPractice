
newline = "Hello\nWorld"
tab = "Hello\tWorld"
backslash = "Hello\\World"
single_quote = "Hello\'World"
double_quote = "Hello\"World"



print(newline)              # Output: Hello
                            #         World
print(tab)                  # Output: Hello   World
print(backslash)            # Output: Hello\World
print(single_quote)         # Output: Hello'World
print(double_quote)         # Output: Hello"World


