
fruits = {"apple", "cherry", "banana"}
my_set = {12.0, ("Elephant", "Lion"), "apple", 1}
empty_set = set()
unique_elements_set = {4,3,2,2,1,2,2,3,1,1,4,3,2}       


print(fruits)                               # Output: {'banana', 'cherry', 'apple'}
print(type(fruits))                         # Output: <class 'set'>

print(my_set)                               # Output: {1, 'apple', 12.0, ('Elephant', 'Lion')}
print(type(fruits))                         # Output: <class 'set'>

print(empty_set)                            # Output: set()
print(type(empty_set))                      # Output: <class 'set'>

print(unique_elements_set)                  # Output: {1, 2, 3, 4}
print(type(unique_elements_set))            # Output: <class 'set'>

